    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tableVitalSigns.getSelectedRow();
        
        if(selectedRow >= 0)
        {
            VitalSigns vs = (VitalSigns)tableVitalSigns.getValueAt(selectedRow, 0);
            bloodPressureTextField.setText(String.valueOf(vs.getBloodPressure()));
            temperatureTextField.setText(String.valueOf(vs.getTemperature()));
            pulseTextField.setText(String.valueOf(vs.getPulse()));
            dateTextField.setText(vs.getDate());
            
        }
        else
            JOptionPane.showMessageDialog(null,"please slect any row");
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
         int selectedRow = tableVitalSigns.getSelectedRow();
        
        if(selectedRow >= 0)
        {
           VitalSigns vs = (VitalSigns)tableVitalSigns.getValueAt(selectedRow, 0);
            vsh.deleteVitals(vs);
            JOptionPane.showMessageDialog(null,"VitalSign has been deleted");
            populateTable();
            
        }
        else
            JOptionPane.showMessageDialog(null,"please slect any row");
        
    }//GEN-LAST:event_jButton2ActionPerformed



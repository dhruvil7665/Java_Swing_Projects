    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRows = tblDirectory.getSelectedRow();
        
        if(selectedRows >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the product?","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Product product = (Product) tblDirectory.getValueAt(selectedRows, 0);
                prodDir.deleteProduct(product);
                populate(prodDir,"all");
            }
            }
           
         else{
            JOptionPane.showMessageDialog(null, "Please select a Product","Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRows = tblDirectory.getSelectedRow();
        
        if(selectedRows < 0){
            JOptionPane.showMessageDialog(null, "Please select a Product","Warning", JOptionPane.WARNING_MESSAGE);
        } else{
            Product product = (Product) tblDirectory.getValueAt(selectedRows, 0);
            ViewPanel viewPanel = new ViewPanel(rightPanel, product, prodDir);
            rightPanel.add("ViewPanel",viewPanel);
            CardLayout layout = (CardLayout) rightPanel.getLayout();
            layout.next(rightPanel);
        }
        
    }//GEN-LAST:event_btnViewActionPerformed


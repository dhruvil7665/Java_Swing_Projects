    public void populate(ProductDirectory prodDir, String s){
        DefaultTableModel dtm = (DefaultTableModel)tblDirectory.getModel();
        dtm.setRowCount(0);
         
        for(Product a : prodDir.getProductDirectory()){
               if(a.getName().equals(s)||s.equals("all")){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0]=a;
            row[1]=a.getAvailNum();
            row[2]=a.getPrice();
            dtm.addRow(row);}
        }
            
          
          
    }


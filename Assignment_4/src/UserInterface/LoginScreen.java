    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        
        userName = txtUserName.getText();
        char[] passwordChar = txtPassword.getPassword();
        password = String.valueOf(passwordChar);
        if(userName.isEmpty()){
            JOptionPane.showMessageDialog(null, "User name cannot be empty");
            return;
        }
        if(password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Password cannot be empty");
            return;
        }
        if(userName.equals("admin") && password.equals("admin")){
            for(Component c: jPanelLeft.getComponents()){
                if(c instanceof JButton){
                    JButton button = (JButton) c;
                    if(button.getName().equals("btnManageAirliner") || button.getName().equals("btnManageFlights") || button.getName().equals("btnViewBookingHistory")){
                        button.setVisible(true);
                    }
                    else if(button.getName().equals("btnBookFlight")){
                        button.setVisible(false);
                    }
                }
            }
            txtUserName.setText("");
            txtPassword.setText("");
            CardLayout layout = (CardLayout)jPanelRight.getLayout();
            jPanelRight.add(new SuccessScreen(jPanelRight, jPanelLeft, userName));
            layout.next(jPanelRight);
        }
        else{
            boolean flag = false;
            for(User u: customerDirectory.getCustomerDirectory()){
                Customer c = (Customer) u;
                if(c.getUserName().equals(userName)){
                    flag = true;
                    if(c.verify(password)){
                        //SuccessSreen
                        for(Component comp: jPanelLeft.getComponents()){
                            if(comp instanceof JButton){
                                JButton button = (JButton) comp;
                                if(button.getName().equals("btnManageAirliner") || button.getName().equals("btnManageFlights")){
                                    button.setVisible(false);
                                }
                                else if(button.getName().equals("btnBookFlight") || button.getName().equals("btnViewBookingHistory")){
                                    button.setVisible(true);
                                }
                            }
                            MainJFrame.setUserName(c);
                        }
                        txtUserName.setText("");
                        txtPassword.setText("");
                        CardLayout layout = (CardLayout)jPanelRight.getLayout();
                        jPanelRight.add(new SuccessScreen(jPanelRight, jPanelLeft, userName));
                        layout.next(jPanelRight);
                    } else{
                        JOptionPane.showMessageDialog(null, "Please enter correct password");
                        //txtUserName.setText("");
                        txtPassword.setText("");
                    }
                }
            }
            if(flag == false){
                JOptionPane.showMessageDialog(null, "User doesn't exist");
                txtUserName.setText("");
                txtPassword.setText("");
                return;
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed


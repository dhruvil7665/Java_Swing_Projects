    public void deleteFlights(Airliner airliner){
        System.out.println("in delete");
        
        ArrayList<Flight> temp = new ArrayList<Flight>();
        for(Flight f : flightDir.getFlightList()){
            if(f.getAirlinerName().getAirlinerName().equals(airliner.getAirlinerName())){
                temp.add(f);
            }
        }
        flightDir.getFlightList().removeAll(temp);
//     
    }
    
    private void btnDeleteAirlinerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAirlinerActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblManageAirliner.getSelectedRow();
        System.out.println(selectedRow);
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select an Airliner","Warning", JOptionPane.WARNING_MESSAGE);
        } else{
            boolean flag = false;
            Airliner airliner = (Airliner) tblManageAirliner.getValueAt(selectedRow, 0);
            for(Flight f : flightDir.getFlightList()){
               if(f.getAirlinerName().getAirlinerName().equals(airliner.getAirlinerName())){
                   flag = true;
                   int dialogButton = JOptionPane.YES_NO_OPTION;
                   int dialogResult = JOptionPane.showConfirmDialog(null, "Flights are scheduled for this airliner. \nThis will delete the flights.\nDo you wish to continue?","Warning",dialogButton);
                if(dialogResult == JOptionPane.YES_OPTION){
                    deleteFlights(airliner);
                    airlinerDirectory.deleteAirliner(airliner);
                    JOptionPane.showMessageDialog(null, "Airliner deleted successfully!","Information", JOptionPane.INFORMATION_MESSAGE);
                    populateTable();
                    break;
            }
               } 
            }
            if(flag == false){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the Airliner?","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                airlinerDirectory.deleteAirliner(airliner);
                JOptionPane.showMessageDialog(null, "Airliner deleted successfully!","Information", JOptionPane.INFORMATION_MESSAGE);
                populateTable();
            }
            }
        }
    }//GEN-LAST:event_btnDeleteAirlinerActionPerformed


    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblManageAirliner.getModel();
        dtm.setRowCount(0);
        for(Airliner a : airlinerDirectory.getAirlinerDirectory()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = a;
            row[1] = a.getAirlinerAddress();
            row[2] = a.getAirlinerContactNumber();
            row[3] = a.getTotalFlightsPerDay();
            row[4] = a.getAirlinerRating();
            dtm.addRow(row);
        }
    }


    private void sellPriceSubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sellPriceSubmitBtnActionPerformed
        // TODO add your handling code here:
        try{
            if(sellPriceTxtField.getText().isEmpty() || sellPriceTxtField.getText() == ""){
                JOptionPane.showMessageDialog(null,"Selling Price cannot be empty","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            if(Float.parseFloat(sellPriceTxtField.getText()) < 0){
                JOptionPane.showMessageDialog(null,"Please enter a valid Selling Price","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
        request.setSellingPrice(Float.parseFloat(sellPriceTxtField.getText()));
        request.setApprovedStatus("Completed");
        JOptionPane.showMessageDialog(null,"Selling Price Assigned and Forwarded to Manager","Information",JOptionPane.INFORMATION_MESSAGE);
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Please enter a valid number","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_sellPriceSubmitBtnActionPerformed


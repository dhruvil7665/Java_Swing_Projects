    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        if(nameJTextField.getText() == null || nameJTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Network cannot be empty","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if(!validations.onlyLettersValidation(nameJTextField.getText())){
                JOptionPane.showMessageDialog(null, "Network Name can only contain letters","Warning", JOptionPane.WARNING_MESSAGE);
                return;
        }
        
        String name = nameJTextField.getText();
        Network network = system.createAndAddNetwork();
        network.setName(name);
        populateNetworkTable();
        nameJTextField.setText("");
    }//GEN-LAST:event_submitJButtonActionPerformed

    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed
        userProcessContainer.remove(this);
         Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        SystemAdminWorkAreaJPanel sysAdminwjp = (SystemAdminWorkAreaJPanel) component;
        sysAdminwjp.populateTree();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backJButtonActionPerformed

    private void deleteJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = networkJTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select a Network","Warning", JOptionPane.WARNING_MESSAGE);
        } else{
            Network network = (Network) networkJTable.getValueAt(selectedRow, 0);
            system.deleteNetwork(network);
            populateNetworkTable();
        }
        
    }//GEN-LAST:event_deleteJButtonActionPerformed


    public void populateTrekSlotComboBox() throws ParseException{
        trekSlots.removeAllItems();
        for(TrekSlotAndCost trekSlotAndCost: trek.getTrekSlotAndCost()){
            System.out.println("slot: "+trekSlotAndCost.getTrekStartDate());
            //String strDate = dateFormat.format(trekSlotAndCost.getTrekStartDate());
            Date currentDate = dateFormat.parse(dateFormat.format(new Date()));
            Date startDate = dateFormat.parse(dateFormat.format(trekSlotAndCost.getTrekStartDate()));
            if(startDate.compareTo(currentDate) >= 0){
                trekSlots.addItem(trekSlotAndCost);
            }
        }
    }


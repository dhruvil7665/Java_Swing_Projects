    private void bookNowButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookNowButtonActionPerformed
        // TODO add your handling code here:
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        if(trekSlots.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "Please select a slot","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            if(customerTotalSeats.getText() == null || customerTotalSeats.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Please enter Total number of Trekkers","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            int totalTrekkers = Integer.parseInt(customerTotalSeats.getText());
            Date bookedSlot;
            bookedSlot = dateFormat.parse(trekSlots.getSelectedItem().toString());
            for(TrekSlotAndCost trekSlotAndCost: trek.getTrekSlotAndCost()){
                Date trekSlot;
                trekSlot = dateFormat.parse(dateFormat.format(trekSlotAndCost.getTrekStartDate()));
                if(bookedSlot.compareTo(trekSlot) == 0){
                    int availableSeats = trekSlotAndCost.getAvailableSeats();
                    if(totalTrekkers > availableSeats){
                        JOptionPane.showMessageDialog(null, "Less seats available", "Warning", JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                    System.out.println("available seats: "+availableSeats);
                    System.out.println("total trekkers: "+totalTrekkers);
                    trekSlotAndCost.setAvailableSeats(availableSeats-totalTrekkers);
                }
            }

            userProcessContainer.add("CustomerBookingJPanel",new CustomerBookingJPanel(userProcessContainer, account, customerOrganization, enterprise, business,totalTrekkers , trek, trekSlots.getSelectedItem().toString()));
        } catch (ParseException ex) {
            Logger.getLogger(TrekDetailsJPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter a valid number","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        layout.next(userProcessContainer);
    }//GEN-LAST:event_bookNowButtonActionPerformed


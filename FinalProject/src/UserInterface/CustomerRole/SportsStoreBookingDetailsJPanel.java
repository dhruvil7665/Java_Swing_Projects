    public void displayBookingDetails(){
    
        int totalP = 0;
        int totalqty=0;
        double totalCostCount =0;
        for(CustomerItems item : custList){
            totalP++;
            totalqty=totalqty+item.getQuantity();
            totalCostCount=totalCostCount+(item.getProduct().getCostPrice()*item.getQuantity());
        
        }
        
        totalProds.setText(String.valueOf(totalP));
        totalQty.setText(String.valueOf(totalqty));
        double totalCostplusTaxes = 0;
        
        totalCostplusTaxes = totalCostCount+((totalCostCount*6.25)/100);
        
        totalCost.setText(String.valueOf(totalCostplusTaxes));
        
        
    
    }
        

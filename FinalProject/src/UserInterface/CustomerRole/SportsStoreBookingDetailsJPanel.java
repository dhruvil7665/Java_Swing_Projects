    private void confirmBookingButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBookingButton1ActionPerformed
        // TODO add your handling code here:
        
        String cardHolder = ccHolderName1.getText();
        String cardNumber = ccNumber1.getText();
        String expiryDate = expiryDate1.getText()+expiryDate2.getText();
        String cvv = ccCVV.getText();

        if(cardHolder == null || cardHolder.isEmpty()){
            JOptionPane.showMessageDialog(null, "Card Holder Name cannot be empty","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(!validations.onlyLettersValidation(cardHolder)){
            JOptionPane.showMessageDialog(null, "Card Holder Name can only contain letters","Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(cardNumber == null || cardNumber.isEmpty()){
            JOptionPane.showMessageDialog(null, "Card Number cannot be empty","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(!validations.sixteenDigits(cardNumber)){
            JOptionPane.showMessageDialog(null, "Card Number must be of 16 digits","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(expiryDate1.getText() == null || expiryDate1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Expiry Month cannot be empty","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(!validations.twoDigits(expiryDate1.getText())){
            JOptionPane.showMessageDialog(null, "Expiry Month must be of 2 digits","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(expiryDate2.getText() == null || expiryDate2.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Expiry Year cannot be empty","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }

        if(!validations.twoDigits(expiryDate2.getText())){
            JOptionPane.showMessageDialog(null, "Expiry Year must be of 2 digits","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }

        if(cvv == null || cvv.isEmpty()){
            JOptionPane.showMessageDialog(null, "CVV cannot be empty","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(!validations.threeDigits(cvv)){
            JOptionPane.showMessageDialog(null, "CVV must be of 3 digits","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }

        for(CustomerItems item : custList){
            SportsStoreConfirmPaymentRequest request = new SportsStoreConfirmPaymentRequest();
            request.setSender(account);
            request.setStatus("Payment pending for confirmation");
            request.setCardHolderName(cardHolder);
            request.setCardNumber(cardNumber);
            request.setExpiryDate(expiryDate);
            request.setCvv(cvv);
            CustomerOrders ord = new CustomerOrders();
            ord.setProduct(item.getProduct());
            ord.setCustAccount(item.getCustAccount());
            ord.setQuantity(item.getQuantity());
            ord.setOrderDate(new Date());
            request.setCustOrder(ord);
            account.getWorkQueue().getWorkRequestList().add(request);
            for(Organization org: enterprise.getOrganizationDirectory().getOrganizationList()){
                if(org instanceof SportsStorePaymentOrganization){
                    paymentOrganization = (SportsStorePaymentOrganization) org;
                    break;
                }
            }
            if(paymentOrganization != null){
                paymentOrganization.getWorkQueue().getWorkRequestList().add(request);
            }
        }

        JOptionPane.showMessageDialog(null, "Payment sent for confirmation");
        ccHolderName1.setText("");
        ccNumber1.setText("");
        expiryDate1.setText("");
        expiryDate2.setText("");
        ccCVV.setText("");
        JOptionPane.showMessageDialog(null,"Please login again","Information",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_confirmBookingButton1ActionPerformed

